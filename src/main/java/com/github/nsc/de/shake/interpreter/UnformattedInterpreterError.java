package com.github.nsc.de.shake.interpreter;

public class UnformattedInterpreterError extends Error {
    public UnformattedInterpreterError() {
        super();
    }
    public UnformattedInterpreterError(String error, Throwable cause) {
        super(error, cause);
    }
    public UnformattedInterpreterError(String error) {
        super(error);
    }
    public UnformattedInterpreterError(Throwable cause) {
        super(cause);
    }

}
